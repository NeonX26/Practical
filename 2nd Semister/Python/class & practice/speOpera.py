# special operators- Identical operators
# 'is' true if the operands are identicals
# 'is not' true if the operands are not identicals
a,b=12,12
print(a is b)
print(a is not b)



# special operators- Membership operators
#'in' true if values is found in sequence
#'not in' true if values is not found in sequence
x ='Vishal'
print('V' in x)
print('J' not in x)